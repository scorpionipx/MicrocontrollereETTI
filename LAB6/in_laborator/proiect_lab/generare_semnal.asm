#include p16f84.inc

i equ 0x20

main:
	BSF STATUS, RP0; pozitionare bank 1
	MOVLW B'11111100'
	MOVWF TRISB; RB0 output
	BCF STATUS, RP0; revenire in bank 0
	
	MOVLW D'5'
	MOVWF i

	MOVLW B'00000001'
	MOVWF PORTB
	NOP
	LOOP:
	BSF PORTB, 1
	BCF PORTB, 0
	CLRF PORTB
	BSF PORTB, 1
	NOP
	COMF PORTB, 1
	BSF PORTB, 1
	BCF PORTB, 0
	COMF PORTB
	DECFSZ i,1 
	GOTO LOOP
NOP
end

	